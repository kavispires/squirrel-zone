export const randomGroup = (numMembers) => {
  const group = [];

  while (group.length < numMembers) {
    const member = NAMES[Math.floor(Math.random() * NAMES.length)];

    if (!group.includes(member)) {
      group.push(member);
    }
  }

  return group.join(', ');
};
